    protected void btn_Insert_Click(object sender, EventArgs e)
    {
        int result = 0;
        int stock;
        decimal price;
        string image = "";
        if (int.TryParse(tb_StockLevel.Text, out stock) == false)
        {
            ClientScript.RegisterStartupScript(this.GetType(), "Success", "<script type='text/javascript'>alert('Please type in a numeric number for stock level.');window.location='login.aspx';</script>'");
        }
        else if (decimal.TryParse(tb_UnitPrice.Text, out price) == false)
        {
            ClientScript.RegisterStartupScript(this.GetType(), "Success", "<script type='text/javascript'>alert('please ensure that unit prise is in decimal form.');window.location='login.aspx';</script>'");
        }
        else
        {
            if (FileUpload1.HasFile == true)
            {
                image = "img\\" + FileUpload1.FileName;
            }

            Product prod = new Product(tb_ProductID.Text, tb_ProductName.Text, tb_ProductDesc.Text,
                decimal.Parse(tb_UnitPrice.Text), FileUpload1.FileName, int.Parse(tb_StockLevel.Text), tb_Gender.Text, tb_DOR.Text, tb_Brand.Text);
            result = prod.ProductInsert();

            if (result > 0)
            {
                string saveimg = Server.MapPath(" ") + "\\" + image;
                lbl_Result.Text = saveimg.ToString();
                FileUpload1.SaveAs(saveimg);
                //loadProductInfo();
                //loadProduct();
                //clear1();
                Response.Write("<script>alert('Insert successful');</script>");

            }
            else { Response.Write("<script>alert('Insert NOT successful');</script>"); }
            tb_ProductDesc.Text = "";
            tb_ProductID.Text = "";
            tb_ProductName.Text = "";
            tb_StockLevel.Text = "";
            tb_UnitPrice.Text = "";
            tb_Gender.Text = "";
            tb_DOR.Text = "";
            tb_Brand.Text = "";
            FileUpload1 = null;
        }
    }

